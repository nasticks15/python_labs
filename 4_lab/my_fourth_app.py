# 8. Lambda-функції
# =========================
def a_b_func(a, b):
    return a, b

this_is_lambda = lambda first, age: f'Цей код написав: {first}, Мені {age:10d} років'

print("Це просто функція:", a_b_func("a", 1))
print("А це лямбда:", this_is_lambda)
print("Це її виклик:", this_is_lambda('Богдан', 1_00_000))
print("Lambda з розпакуванням функції:", this_is_lambda(*a_b_func("a", 1)))



